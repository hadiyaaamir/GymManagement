    void updateTable() {

        int c;

        try {
            Connection myConn = DriverManager.getConnection(url, user, password);
            Statement myStmt = myConn.createStatement();

            String sql;

                sql = "SELECT `TrainerID`, `Firstname`, "
                        + "`Lastname`, Category "
                        + "FROM `trainer` "
                        + "WHERE Active = 1;";
//            }
            
            ResultSet rs = myStmt.executeQuery(sql);

            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            DefaultTableModel dft = (DefaultTableModel) teamTable.getModel();
            dft.setRowCount(0);

            while (rs.next()) {
                Vector v2 = new Vector();

                for (int i = 1; i <= c; i++) {
                    v2.add(rs.getString("TrainerID"));
                    v2.add(rs.getString("Firstname") + " " + rs.getString("Lastname"));
                    v2.add(rs.getString("Category"));
                }

                dft.addRow(v2);
            }
        } 
        catch (SQLException ex) {
            Logger.getLogger(TeamGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


