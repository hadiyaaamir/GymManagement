    void setValues() {
        try {
            Connection myConn = DriverManager.getConnection(url, user, password);
            Statement myStmt = myConn.createStatement();
            
            String sql = "SELECT `sessionfee`, `category`, `Firstname`, `Lastname`,"
                    + "`description`, Hiredate FROM `trainer` "
                    + "where `trainerID` = '" + MembersTeamGUI.Sid + "';";
            
            ResultSet rs = myStmt.executeQuery(sql);
            
            while(rs.next()) {
               sess.setText(sess.getText() + " Rs "+rs.getString("sessionfee"));
               name.setText(rs.getString("firstname") + " " + rs.getString("lastname"));
               descrip.setText(rs.getString("description"));
               category.setText(rs.getString("category"));
               
               String hiredate = rs.getString("hiredate");
               String y = hiredate.substring(0, 4);
               exp.setText(exp.getText() + " " + y);
               
            }
            
        } 
        
        catch (SQLException ex) {
            Logger.getLogger(ViewTrainerGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
